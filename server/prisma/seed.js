async function main() {
  const categoryMap = {}

  for (const category of categories) {
    const savedCategory = await prisma.serviceCategory.upsert({
      where: { slug: category.slug },
      update: category,
      create: category,
    })

    categoryMap[category.slug] = savedCategory
  }

  const unusedCategorySlugs = [
  'aesthetic-dentistry',
  'surgical-dentistry',
  'pediatric-dentistry',
]

await prisma.serviceCategory.deleteMany({
  where: {
    slug: {
      in: unusedCategorySlugs,
    },
  },
})

  const duplicateTherapyCategory = await prisma.serviceCategory.findUnique({
    where: {
      slug: 'therapeutic-dentistry',
    },
  })

  if (duplicateTherapyCategory) {
    await prisma.service.updateMany({
      where: {
        categoryId: duplicateTherapyCategory.id,
      },
      data: {
        categoryId: categoryMap.therapy.id,
      },
    })

    await prisma.serviceCategory.delete({
      where: {
        id: duplicateTherapyCategory.id,
      },
    })
  }

  for (const service of services) {
    const category = categoryMap[service.categorySlug]

    await prisma.service.upsert({
      where: { slug: service.slug },
      update: {
        categoryId: category.id,
        title: service.title,
        shortDesc: service.shortDesc,
        fullDesc: service.fullDesc,
        price: service.price,
        duration: service.duration,
        image: service.image,
        badge: service.badge,
        sortOrder: service.sortOrder,

        indications: {
          deleteMany: {},
          create: service.indications.map((text, index) => ({
            text,
            sortOrder: index,
          })),
        },

        included: {
          deleteMany: {},
          create: service.included.map((text, index) => ({
            text,
            sortOrder: index,
          })),
        },

        steps: {
          deleteMany: {},
          create: service.steps.map((step, index) => ({
            title: step.title,
            text: step.text,
            sortOrder: index,
          })),
        },

        faqs: {
          deleteMany: {},
          create: service.faqs.map((faq, index) => ({
            question: faq.question,
            answer: faq.answer,
            sortOrder: index,
          })),
        },
      },
      create: {
        categoryId: category.id,
        title: service.title,
        slug: service.slug,
        shortDesc: service.shortDesc,
        fullDesc: service.fullDesc,
        price: service.price,
        duration: service.duration,
        image: service.image,
        badge: service.badge,
        sortOrder: service.sortOrder,

        indications: {
          create: service.indications.map((text, index) => ({
            text,
            sortOrder: index,
          })),
        },

        included: {
          create: service.included.map((text, index) => ({
            text,
            sortOrder: index,
          })),
        },

        steps: {
          create: service.steps.map((step, index) => ({
            title: step.title,
            text: step.text,
            sortOrder: index,
          })),
        },

        faqs: {
          create: service.faqs.map((faq, index) => ({
            question: faq.question,
            answer: faq.answer,
            sortOrder: index,
          })),
        },
      },
    })
  }

  console.log('Services seed completed successfully')
}

